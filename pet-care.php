                <p>0-4 weeks – At this age, it is recommended that puppies should not be subjected to any type of routine or forced exercise.
                    It would be best to leave the puppy alone so he can control his own pace. Usually, a puppy will choose to play around and then
                    stop when he gets tired. This play-and-rest style is very important to protect your puppy from overworking his joints and ligaments.
                    Since they muscles are also not yet that developed, they will not be able to withstand prolonged physical exercise.
                    Your puppy will be spending a lot of time sleeping at this age. You will also notice that when you try to walk him farther
                    away from his kennel or from your home, he will resist the pull. This is due to the fact that your puppy is still
                    feeling his way around his territory and taking him away from it will scare and even confuse him.
