                <p>13 weeks – 6 months – At this stage, your puppy will have greater resilience to physical activities and you may mistake
                    this as a sign that your puppy can now do a lot of physical training or exercise.But a word of caution here:
                    your puppy’s growth plates and ligaments are still tender and prone to damage so it would be wise if you can let them
                    do their own thing. At six months old, they can now spend a maximum of 30 minutes of either leash walking or even
                    a bit of jogging around the block. You can divide this into two sessions containing 15 minutes each during the morning
                    and the afternoon before they go to bed.
                </p>
                <p>7 – 10 months – Your puppy will now be almost full-grown and they can now engage in longer exercises but the key here is to
                    never engage them in heavy impact activities such as jumping or even climbing up stairs. The growth plates may still be in their
                    developmental stage and engaging them in heavy or exhausting activities can damage their joints. When this happens, the bones
                    will continue to grow longer but the growth plates will stop developing which can lead to a bow-leg or hip dysplasia.
                    The best types of activities to engage your puppy in will include swimming as it is a low-impact activity and will do no
                    damage to joints
                </p>

